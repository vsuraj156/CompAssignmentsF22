class Company:

    sales = []
    prices = {}
    employees = []

    def __init__(self, name):
        self.name = name
    
    def add_product(name, price):
        sales.append(name)
        prices[name] = price
    
    def add_employee(employee):
        employees.append(employee)


class Employee:
    def __init(self, name, company, role=1):
        self.name = name
        self.company = company
        self.role = role